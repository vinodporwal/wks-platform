package com.wks.caseengine.rest.entity;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.Table;

@Entity
@Table(name = "functional_location")
public class FunctionalLocation {

	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id; 
	
	@Column(name = "asset_no")
	private String assetNo;
	
	@Column(name = "asset_sort_feild")
	private String assetSortField;
	
	@Column(name = "uas_asset_name")
    private String uasAssetName;
	
	@Column(name = "parent_fl_name")
    private String parentFLName;
	
	@Column(name = "asset_fl")
    private String assetFL;
	
	@Column(name = "uas_display_name")
    private String uasDisplayName;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getAssetNo() {
		return assetNo;
	}

	public void setAssetNo(String assetNo) {
		this.assetNo = assetNo;
	}

	public String getAssetSortField() {
		return assetSortField;
	}

	public void setAssetSortField(String assetSortField) {
		this.assetSortField = assetSortField;
	}

	public String getUasAssetName() {
		return uasAssetName;
	}

	public void setUasAssetName(String uasAssetName) {
		this.uasAssetName = uasAssetName;
	}

	public String getParentFLName() {
		return parentFLName;
	}

	public void setParentFLName(String parentFLName) {
		this.parentFLName = parentFLName;
	}

	public String getAssetFL() {
		return assetFL;
	}

	public void setAssetFL(String assetFL) {
		this.assetFL = assetFL;
	}

	public String getUasDisplayName() {
		return uasDisplayName;
	}

	public void setUasDisplayName(String uasDisplayName) {
		this.uasDisplayName = uasDisplayName;
	}
}
